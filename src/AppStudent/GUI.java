    private void tableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableStudentMouseClicked
        DefaultTableModel model = (DefaultTableModel) this.tableStudent.getModel();
        this.txtID.setText(model.getValueAt(this.tableStudent.getSelectedRow(), 0).toString());
        this.txtName.setText(model.getValueAt(this.tableStudent.getSelectedRow(), 1).toString());
        boolean checkM = model.getValueAt(this.tableStudent.getSelectedRow(), 2).toString().contains("M") ? true : false;
        this.cbM.setSelected(checkM);
        boolean checkF = model.getValueAt(this.tableStudent.getSelectedRow(), 2).toString().contains("F") ? true : false;
        this.cbF.setSelected(checkF);
        this.txtClass.setText(model.getValueAt(this.tableStudent.getSelectedRow(), 3).toString());
        this.txtAddress.setText(model.getValueAt(this.tableStudent.getSelectedRow(), 4).toString());
        this.txtEmail.setText(model.getValueAt(this.tableStudent.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_tableStudentMouseClicked

