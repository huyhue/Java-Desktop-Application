    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String Sno = this.txtID.getText();
            String Sname = this.txtName.getText();
            String Sgender = this.cbF.isSelected() ? "F" : "M";
            String Sclass = this.txtClass.getText();
            String Sadress = this.txtAddress.getText();
            String Semail = this.txtEmail.getText();

            // Kiểm tra dữ liệu nhập vào, kiểm tra trùng khóa chính trong database
            if (manager.checkStudent(Sno)) {
                throw new Exception("This student ID is already exits in database!");
            }
            if (!Validation.checkIdFormat(Sno)) {
                throw new Exception("Id of student can not contain space!");
            }
            if (!Validation.checkNameFormat(Sname)) {
                throw new Exception("Name of student can not contain special(s) character!");
            }
            if (!Validation.checkIdFormat(Sclass)) {
                throw new Exception("Name of class can not contain special(s) character!");
            }
            if (!Validation.checkNameFormat(Sadress)) {
                throw new Exception("Adress can not contain special(s) character!");
            }

            // Thêm dữ liệu vào database
            manager.addNew(Sno, Sname, Sgender, Sclass, Sadress, Semail);

            // Cập nhật hiển thị database cho phần mềm
            data = manager.getAll();
            ((DefaultTableModel) (tableStudent.getModel())).setDataVector(data, header);

            // Thông báo thành công
            JOptionPane.showMessageDialog(contentPane, "Add Success!", "Sucess", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e1) {
            JOptionPane.showMessageDialog(contentPane, "Add new failure\nDetails: " + e1, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            String Sno = txtID.getText();

            // Kiểm tra sinh viên có trong database hay không
            if (!manager.checkStudent(Sno)) {
                throw new Exception("This student ID is not exits in database, So can delete!");
            }
            manager.delete(Sno);

            // Cập nhật lại dữ liệu hiển thị trên phần mềm
            data = manager.getAll();
            ((DefaultTableModel) (tableStudent.getModel())).setDataVector(data, header);

            // Thông báo xóa thành công
            JOptionPane.showMessageDialog(contentPane, "Delete Success!", "Sucess", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e1) {
            JOptionPane.showMessageDialog(contentPane, "Delete failure\nDetails:" + e1, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

